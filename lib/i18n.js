/**
 * Get a translated string.
 * Supports placeholders: {n}, {done}, {total}, {lang}
 */
export function t(key, lang, params) {
  const str = STRINGS[lang]?.[key] || STRINGS.en[key] || key;
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? `{${k}}`);
}

/**
 * Apply translations to all elements with data-i18n attributes.
 * - data-i18n="key"          → sets textContent
 * - data-i18n-placeholder="key" → sets placeholder
 * - data-i18n-title="key"    → sets title attribute
 */
export function applyI18n(lang) {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n, lang);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder, lang);
  });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle, lang);
  });
}

/**
 * Read targetLang from storage, apply i18n, return the language code.
 */
export async function getUILang() {
  const { targetLang } = await chrome.storage.sync.get("targetLang");
  return targetLang || "zh-CN";
}

